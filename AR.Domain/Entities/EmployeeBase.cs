namespace AR.Domain.Entities
{
    public class EmployeeBase
    {
        public EmployeeBase(Guid id, string name, string email)
        {
            Id = id;
            Name = name;
            Email = email;
        }

        public Guid Id { get; set; }
        public string Name { get; set; }
        public string Email { get; set; }
    }
}
